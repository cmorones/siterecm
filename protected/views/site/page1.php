		        <div class="default">
		        	<p>
		        		El presente Reporte es un instrumento de información pública, puesto a disposición de los ciudadanos y de los actores económicos del Distrito Federal, elaborado por la Secretaría de Desarrollo Económico del Gobierno del Distrito Federal.
		        	<p>
		        	<p>
		        		Entre los objetivos que se persiguen con el Reporte Económico están:
		        	<p>
		        	<p>
		        		<ul>
		        			<li>Que la sociedad mexicana cuente con información cierta y actualizada acerca de la evolución económica de la capital, provista de manera oficial por sus autoridades;</li>
		        			<li>Que los actores económicos tomen sus decisiones con la mejor y más amplia información disponible;</li>
		        			<li>Que, con base en esta información, los responsables de la política económica en la Capital puedan construir sistemas de información más complejos y útiles (estudios prospectivos, sistemas de alarma temprana, modelos de comportamiento, decisiones estratégicas para el crecimiento).</li>
		        			<li>Que el área económica del GDF despliegue una política de información pública relevante y una política de transparencia activa. En otras palabras, convertir a la información económica en parte del derecho a la información de los capitalinos.</li>
		        		</ul>
		        	<p>
		        	<p>
		        		De este modo, con esta cuarta entrega, se realiza un balance anual de la economía durante el 2013 con base en indicadores de coyuntura, los cuales se agrupan en los apartados de producción, precios, empleo, salarios e ingreso, empresas, inversión, finanzas, comercio, turismo e insumos básicos para la actividad económica. 
		        	<p>
		        	<p>
		        		Especial mención merece el tema de la aprobación por unanimidad en la Asamblea Legislativa del Distrito Federal de la Ley para el Desarrollo Económico del Distrito Federal, como el primer paso para alcanzar el objetivo mayor de una Ciudad autónoma y dueña de las palancas de su propio desarrollo al constituir un nuevo marco para el crecimiento de una gran Ciudad. En este Reporte se muestra una infografía que ofrece algunas claves para entender esta nueva Ley.
		        	<p>
		        	<p>
		        		Finalmente, y con el objetivo de expandir y consolidar una cultura económica propia de la Ciudad de México, se presenta otra infografía que da cuenta de algunos de los aspectos más relevantes en la Ciudad: <a href="apendice.html">“Las 5 fortalezas de la economía del Distrito Federal"</a>.
		        	<p>
		        	<p>
		        		La Secretaría de Desarrollo Económico, procura así, cumplir con las instrucciones del Jefe de Gobierno, Miguel Ángel Mancera, para desarrollar un sistema de información actualizado, sólido y robusto, que sea el fundamento de las decisiones y de la política económica en la Capital de la República.
